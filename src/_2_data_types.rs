pub fn main() {
    let int_val: i32 = 10;
    println!("Integer value: {}", int_val);

    let float_val: f64 = 3.14;
    println!("Float value: {}", float_val);

    let bool_val: bool = true;
    println!("bool value: {}", bool_val);

    let char_val: char = 'R';
    println!("char value: {}", char_val);
}
