pub fn main() {
    let x: i32 = 5;
    let y: i32 = 10;
    // Arithmatic
    println!("x + y = {}", x + y);
    println!("x - y = {}", x - y);
    println!("x * y = {}", x * y);
    println!("x / y = {}", y / x);

    // Reminders
    println!("x % y = {}", y % x);

    let a: f64 = 5.0;
    let b: f64 = 2.0;
    println!("a / b = {}", a / b);
}
